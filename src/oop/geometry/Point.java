package oop.geometry;

public class Point {

    // variables (x; y)
    private int x;
    private int y;

    // constructor (x, y)
    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    // getX(), getY()
    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }



}

